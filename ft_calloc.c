#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(count * size);
	if (!ptr)
		return (NULL);
	ft_memset(ptr, 0, (count * size));
	return (ptr);
}

/* The function calloc allocates the requested memory and returns
a pointer to it. The difference in malloc and calloc is that malloc
does not set the memory to zero where as calloc sets allocated 
memory to zero. */