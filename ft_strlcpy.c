#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *str2, size_t size)
{
	char	*src;
	size_t	i;

	src = (char *)str2;
	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	if (i < size)
		dest[i] = '\0';
	return (ft_strlen(src));
}
